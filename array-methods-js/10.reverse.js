/*
 O método reverse é um método que inverte a ordem dos elementos de um array. 
 Ele modifica o array original e retorna o mesmo array, com os elementos agora na ordem inversa.
*/

const crazyNumbers = [ 1, 2, 3, 4, 5 ];

crazyNumbers.reverse();

console.log(crazyNumbers); // Output: [5, 4, 3, 2, 1]

