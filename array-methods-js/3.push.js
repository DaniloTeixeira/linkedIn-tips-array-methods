/* A função push é usada para adicionar um ou mais elementos ao final de um array. Ela é uma das operações mais comuns realizadas em arrays.

A sintaxe da função push é a seguinte:

array.push(item1, item2, ..., itemX);

Onde array é o nome do array que você deseja adicionar elementos, e item1, item2, ..., itemX são os elementos que você deseja adicionar ao final do array. */

const fruits = [ 'apple', 'banana' ];

fruits.push('orange', 'grapes');

console.log(fruits); // ["apple", "banana", "orange", "grapes"]

/* Neste exemplo, primeiro criamos um array fruits com dois elementos, apple e banana. Em seguida, usamos a função push para adicionar mais dois elementos, orange e grapes, ao final do array fruits. */