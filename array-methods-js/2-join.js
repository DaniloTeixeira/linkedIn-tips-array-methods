/* A função join() do JavaScript é usada para juntar todos os elementos de um array em uma string, separados por um delimitador especificado. Ela retorna uma nova string com os elementos juntos. */

const cars = [ "Ferrari", "Porsche", "Mercedes" ];
const carList = cars.join(", ");

console.log(carList); // "Ferrari, Porsche, Mercedes"

/* Neste exemplo, a função join() é usada para juntar todos os elementos do array cars em uma string, separando-os com uma vírgula e um espaço. */